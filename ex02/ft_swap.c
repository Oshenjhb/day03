#include <unistd.h>
#include <stdio.h>
 void ft_swap(int *a, int *b)
{
	int c;
	 
	c = *a;
	*a = *b;
	*b = c;
}
int main()
{
	int a;
	int b;
	a = 21;
	b = 16;
	ft_swap(&a, &b);
	printf("%d, %d\n", a, b);
	return (0);
}
